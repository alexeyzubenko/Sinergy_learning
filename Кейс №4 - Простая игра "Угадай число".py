import random

def simple_guessing_game():
    """
    –û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ—Å—Ç–æ–π –∏–≥—Ä—ã "–£–≥–∞–¥–∞–π —á–∏—Å–ª–æ".
    """
    # 1. –ó–∞–≥–∞–¥—ã–≤–∞–µ–º —Å–ª—É—á–∞–π–Ω–æ–µ —á–∏—Å–ª–æ –∏ —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –ª–∏–º–∏—Ç –ø–æ–ø—ã—Ç–æ–∫
    secret_number = random.randint(1, 100)
    max_attempts = 5  # –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ø—ã—Ç–æ–∫
    attempts = 0

    print("--- –ü—Ä–æ—Å—Ç–∞—è –∏–≥—Ä–∞ '–£–≥–∞–¥–∞–π —á–∏—Å–ª–æ' ---")
    print(f"–Ø –∑–∞–≥–∞–¥–∞–ª —á–∏—Å–ª–æ –æ—Ç 1 –¥–æ 100. –£ —Ç–µ–±—è –µ—Å—Ç—å {max_attempts} –ø–æ–ø—ã—Ç–æ–∫.")

    # –û—Å–Ω–æ–≤–Ω–æ–π —Ü–∏–∫–ª –∏–≥—Ä—ã
    while attempts < max_attempts:
        # 2. –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –≤–≤–æ–¥ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        try:
            guess_str = input(f"\n–ü–æ–ø—ã—Ç–∫–∞ #{attempts + 1}: –í–≤–µ–¥–∏ —Å–≤–æ–µ —á–∏—Å–ª–æ: ")
            guess = int(guess_str)
        except ValueError:
            print("–û—à–∏–±–∫–∞: –ø–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ.")
            continue # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –æ—Å—Ç–∞—Ç–æ–∫ —Ü–∏–∫–ª–∞ –∏ –ø–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–π –ø–æ–ø—ã—Ç–∫–µ

        attempts += 1

        # 3. –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø—Ä–µ–¥–ø–æ–ª–æ–∂–µ–Ω–∏–µ –∏ –¥–∞–µ–º –ø–æ–¥—Å–∫–∞–∑–∫—É
        if guess < secret_number:
            print("–°–ª–∏—à–∫–æ–º –º–∞–ª–µ–Ω—å–∫–æ–µ —á–∏—Å–ª–æ.")
        elif guess > secret_number:
            print("–°–ª–∏—à–∫–æ–º –±–æ–ª—å—à–æ–µ —á–∏—Å–ª–æ.")
        else:
            # 4. –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —É–≥–∞–¥–∞–ª
            print(f"üéâ –ü–æ–∑–¥—Ä–∞–≤–ª—è—é! –¢—ã —É–≥–∞–¥–∞–ª —á–∏—Å–ª–æ {secret_number} –∑–∞ {attempts} –ø–æ–ø—ã—Ç–æ–∫!")
            return # –ó–∞–≤–µ—Ä—à–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é ( –∏ –∏–≥—Ä—É)

    # 5. –ï—Å–ª–∏ —Ü–∏–∫–ª –∑–∞–≤–µ—Ä—à–∏–ª—Å—è, –∑–Ω–∞—á–∏—Ç, –ø–æ–ø—ã—Ç–∫–∏ –∑–∞–∫–æ–Ω—á–∏–ª–∏—Å—å
    print("\n--- –ò–≥—Ä–∞ –æ–∫–æ–Ω—á–µ–Ω–∞ ---")
    print(f"–£–≤—ã, —É —Ç–µ–±—è –∑–∞–∫–æ–Ω—á–∏–ª–∏—Å—å –ø–æ–ø—ã—Ç–∫–∏. –Ø –∑–∞–≥–∞–¥—ã–≤–∞–ª —á–∏—Å–ª–æ {secret_number}.")

# –ó–∞–ø—É—Å–∫ –∏–≥—Ä—ã
if __name__ == "__main__":
    simple_guessing_game()
